export function NavbarSimple() {
  const [openNav, setOpenNav] = React.useState(false);
 
  const handleWindowResize = () =>
    window.innerWidth >= 960 && setOpenNav(false);
 
  React.useEffect(() => {
    window.addEventListener("resize", handleWindowResize);
 
    return () => {
      window.removeEventListener("resize", handleWindowResize);
    };
  }, []);
 
  return (
    <Navbar className="w-full" fullWidth>
      <div className="flex items-center justify-between text-blue-gray-900">
        <SidebarWithBurgerMenu className=" z-10"/>
        <Typography
          as="a"
          href="#"
          variant="h6"
          className="mr-auto cursor-pointer py-1.5"
        >
          Chapter Name
        </Typography>
        <div className="hidden lg:block mr-auto">
          <NavList />
        </div>
        <IconButton
          variant="text"
          className="mx-auto h-6 w-6 text-inherit hover:bg-transparent focus:bg-transparent active:bg-transparent lg:hidden"
          ripple={false}
          onClick={() => setOpenNav(!openNav)}
        >
          {openNav ? (
            <XMarkIcon className="h-6 w-6" strokeWidth={2} />
          ) : (
            <Bars3Icon className="h-6 w-6" strokeWidth={2} />
          )}
        </IconButton>
      </div>
    </Navbar>

  );
}